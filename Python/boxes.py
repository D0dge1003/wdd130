print("> python boxes.py")
import math

items = int(input("Enter the number of items: "))
perbox = int(input("Enter the number of items per box: "))
# compute and print the number of boxes necessary to hold the items
if perbox <=0: 
    print("Error: The number of items per box must be greater than zero")
else:
    boxes = items / perbox

print(f"For {items} items, packing {perbox} itmes in each box, you will need {math.ceil(boxes)} boxes.")

